export function UserGuidePage() {
  const [query, setQuery] = useState('')
  const [collapsedSectionIds, setCollapsedSectionIds] = useState<Set<string>>(() => new Set())
  const [activeSectionId, setActiveSectionId] = useState(() => DISPLAY_GUIDE_SECTIONS[0]?.id ?? '')
  const contentScrollRef = useRef<HTMLDivElement | null>(null)
  const normalizedQuery = normalizeGuideText(query)
  const filteredSections = useMemo(() => {
    if (!normalizedQuery) return DISPLAY_GUIDE_SECTIONS
    return DISPLAY_GUIDE_SECTIONS.filter((section) => getSectionSearchText(section).includes(normalizedQuery))
  }, [normalizedQuery])

  const totalMatches = filteredSections.length

  useEffect(() => {
    if (filteredSections.length === 0) {
      setActiveSectionId('')
      return
    }
    if (!filteredSections.some((section) => section.id === activeSectionId)) {
      setActiveSectionId(filteredSections[0].id)
    }
  }, [activeSectionId, filteredSections])

  useEffect(() => {
    const container = contentScrollRef.current
    if (!container) return

    let animationFrame = 0

    const updateActiveSection = () => {
      const articles = Array.from(container.querySelectorAll<HTMLElement>('article[id]'))
      if (articles.length === 0) return

      const containerTop = container.getBoundingClientRect().top
      const activationOffset = 96
      let nextActiveId = articles[0].id

      for (const article of articles) {
        const top = article.getBoundingClientRect().top - containerTop
        if (top <= activationOffset) nextActiveId = article.id
        else break
      }

      setActiveSectionId((current) => (current === nextActiveId ? current : nextActiveId))
    }

    const requestUpdate = () => {
      window.cancelAnimationFrame(animationFrame)
      animationFrame = window.requestAnimationFrame(updateActiveSection)
    }

    requestUpdate()
    container.addEventListener('scroll', requestUpdate, { passive: true })
    window.addEventListener('resize', requestUpdate)

    return () => {
      window.cancelAnimationFrame(animationFrame)
      container.removeEventListener('scroll', requestUpdate)
      window.removeEventListener('resize', requestUpdate)
    }
  }, [filteredSections, collapsedSectionIds])

  function handleDownloadPdf() {
    const previousTitle = document.title
    const restoreTitle = () => {
      document.title = previousTitle
      window.removeEventListener('afterprint', restoreTitle)
    }

    document.title = USER_GUIDE_PDF_TITLE
    window.addEventListener('afterprint', restoreTitle)
    window.print()
    window.setTimeout(restoreTitle, 1000)
  }

  function toggleSection(sectionId: string) {
    setCollapsedSectionIds((current) => {
      const next = new Set(current)
      if (next.has(sectionId)) next.delete(sectionId)
      else next.add(sectionId)
      return next
    })
  }

  function collapseVisibleSections() {
    setCollapsedSectionIds((current) => {
      const next = new Set(current)
      filteredSections.forEach((section) => next.add(section.id))
      return next
    })
  }

  function expandVisibleSections() {
    setCollapsedSectionIds((current) => {
      const next = new Set(current)
      filteredSections.forEach((section) => next.delete(section.id))
      return next
    })
  }

  return (
    <div className="user-guide-page min-w-0 space-y-4 pb-8">
      <section className="rounded-md border border-slate-200 bg-white print:block print:max-h-none print:overflow-visible xl:flex xl:max-h-[calc(100vh-7rem)] xl:flex-col xl:overflow-hidden">
        <div className="shrink-0 border-b border-slate-100 bg-slate-50 px-5 py-5">
          <div className="flex flex-col gap-4 xl:flex-row xl:items-start xl:justify-between">
            <div className="max-w-4xl">
              <div className="flex items-center gap-2">
                <BookOpenText className="h-5 w-5 text-slate-500" />
                <h2 className="text-xl font-semibold text-slate-950">Руководство пользователя</h2>
              </div>
              <p className="mt-2 text-sm leading-6 text-slate-600">
                Подробное описание разделов, кнопок, правил, подсказок, задач диспетчера и документов. Используйте поиск, если нужен конкретный
                сценарий.
              </p>
            </div>
            <Button type="button" className="gap-2 print:hidden" onClick={handleDownloadPdf}>
              <Download className="h-4 w-4" />
              Скачать PDF
            </Button>
          </div>
        </div>

        <div className="grid gap-4 px-5 py-4 print:block print:overflow-visible xl:min-h-0 xl:flex-1 xl:grid-cols-[320px_minmax(0,1fr)] xl:overflow-hidden">
          <aside className="self-start print:hidden xl:h-full xl:self-stretch xl:overflow-hidden">
            <div className="space-y-3 xl:flex xl:h-full xl:flex-col xl:overflow-hidden">
              <label className="grid gap-1 text-xs font-medium text-slate-600">
                Поиск по руководству
                <div className="relative">
                  <Search className="pointer-events-none absolute left-3 top-2.5 h-4 w-4 text-slate-400" />
                  <Input
                    value={query}
                    onChange={(event) => setQuery(event.target.value)}
                    placeholder="Например: отменен, ПСТО, клеймо"
                    className="h-9 rounded-md border-slate-200 pl-9 text-sm"
                  />
                </div>
              </label>
              <div className="rounded-md border border-slate-200 bg-slate-50 p-3 text-xs leading-5 text-slate-600">
                {normalizedQuery ? `Найдено разделов: ${totalMatches}` : 'Оглавление'}
              </div>
              <div className="grid grid-cols-2 gap-2">
                <button
                  type="button"
                  className="rounded-md border border-slate-200 bg-white px-2 py-1.5 text-xs font-semibold text-slate-600 hover:bg-slate-50 hover:text-slate-950"
                  onClick={collapseVisibleSections}
                >
                  Свернуть все
                </button>
                <button
                  type="button"
                  className="rounded-md border border-slate-200 bg-white px-2 py-1.5 text-xs font-semibold text-slate-600 hover:bg-slate-50 hover:text-slate-950"
                  onClick={expandVisibleSections}
                >
                  Развернуть все
                </button>
              </div>
              <nav className="max-h-[calc(100vh-12rem)] space-y-1 overflow-auto pr-1 xl:min-h-0 xl:max-h-none xl:flex-1">
                {filteredSections.map((section) => (
                  <a
                    key={section.id}
                    href={`#${section.id}`}
                    onClick={() => setActiveSectionId(section.id)}
                    aria-current={activeSectionId === section.id ? 'true' : undefined}
                    className={`block rounded-md px-3 py-2 text-sm font-medium transition-colors ${
                      activeSectionId === section.id
                        ? 'border border-sky-100 bg-sky-50 text-slate-900 shadow-sm shadow-sky-100/50'
                        : 'border border-transparent text-slate-600 hover:bg-slate-50 hover:text-slate-950'
                    }`}
                  >
                    {section.title}
                  </a>
                ))}
              </nav>
            </div>
          </aside>

          <div
            ref={contentScrollRef}
            className="min-w-0 space-y-4 print:h-auto print:overflow-visible print:pr-0 xl:h-full xl:overflow-y-auto xl:pr-2"
          >
            {filteredSections.length > 0 ? (
              filteredSections.map((section) => (
                <GuideSectionCard
                  key={section.id}
                  section={section}
                  query={query}
                  isCollapsed={collapsedSectionIds.has(section.id)}
                  onToggle={() => toggleSection(section.id)}
                />
              ))
            ) : (
              <div className="rounded-md border border-dashed border-slate-200 bg-slate-50 p-8 text-center text-sm text-slate-500">
                По этому запросу ничего не найдено. Попробуйте другое слово или очистите поиск.
              </div>
            )}
          </div>
        </div>
      </section>
    </div>
  )
}

function GuideSectionCard({
  section,
  query,
  isCollapsed,
  onToggle,
}: {
  section: GuideSection
  query: string
  isCollapsed: boolean
  onToggle: () => void
}) {
  return (
    <article id={section.id} className="scroll-mt-24 rounded-md border border-slate-200 bg-white">
      <button
        type="button"
        className="block w-full border-b border-slate-100 bg-slate-50 px-5 py-4 text-left transition-colors hover:bg-slate-100 print:cursor-default"
        onClick={onToggle}
        aria-expanded={!isCollapsed}
        aria-controls={`guide-section-${section.id}`}
      >
        <span className="flex items-start justify-between gap-3">
          <span className="min-w-0">
            <span className="block text-lg font-semibold text-slate-950">{section.title}</span>
            <span className="mt-2 block text-sm leading-6 text-slate-600">{highlightGuideText(section.summary, query)}</span>
          </span>
          <span className="mt-0.5 inline-flex items-center gap-1.5 rounded-md border border-slate-200 bg-white px-2.5 py-1.5 text-xs font-semibold text-slate-600 print:hidden">
            <ChevronDown className={`h-4 w-4 transition-transform ${isCollapsed ? '-rotate-90' : ''}`} />
            {isCollapsed ? 'Развернуть' : 'Свернуть'}
          </span>
        </span>
      </button>
      <div id={`guide-section-${section.id}`} className={`space-y-4 px-5 py-5 ${isCollapsed ? 'hidden print:block' : ''}`}>
        {section.id === 'sidebar' ? <GuideSidebarMock /> : null}
        {section.id === 'journal' ? <GuideTableMock /> : null}
        {section.id === 'lnk-psto' ? <GuideModalMock /> : null}
        {section.blocks.map((block, index) => (
          <GuideBlockView key={index} block={block} query={query} />
        ))}
      </div>
    </article>
  )
}

function GuideBlockView({ block, query }: { block: GuideBlock; query: string }) {
  if (block.type === 'p') {
    return <p className="text-sm leading-6 text-slate-600">{highlightGuideText(block.text, query)}</p>
  }

  if (block.type === 'list') {
    return (
      <ul className="grid gap-2 text-sm leading-6 text-slate-600">
        {block.items.map((item) => (
          <li key={item} className="flex gap-2">
            <span className="mt-2 h-1.5 w-1.5 shrink-0 rounded-full bg-slate-400" />
            <span>{highlightGuideText(item, query)}</span>
          </li>
        ))}
      </ul>
    )
  }

  if (block.type === 'table') {
    return (
      <div className="guide-table-block">
        {block.title ? <h4 className="mb-2 text-sm font-semibold text-slate-900">{block.title}</h4> : null}
        <div className="overflow-hidden rounded-md border border-slate-200">
          <div className="overflow-x-auto">
            <table className="w-full min-w-[760px] border-collapse text-sm">
              <thead className="bg-slate-100 text-slate-700">
                <tr>
                  {block.table.columns.map((column) => (
                    <th key={column} className="border-r border-white px-3 py-2 text-left font-semibold last:border-r-0">
                      {column}
                    </th>
                  ))}
                </tr>
              </thead>
              <tbody>
                {block.table.rows.map((row, rowIndex) => (
                  <tr key={rowIndex} className="border-t border-slate-100 odd:bg-white even:bg-slate-50/70">
                    {row.map((cell, cellIndex) => (
                      <td key={`${rowIndex}-${cellIndex}`} className="border-r border-slate-100 px-3 py-2 align-top leading-6 text-slate-600 last:border-r-0">
                        {highlightGuideText(cell, query)}
                      </td>
                    ))}
                  </tr>
                ))}
              </tbody>
            </table>
          </div>
        </div>
      </div>
    )
  }

  if (block.type === 'flow') {
    return (
      <div className="rounded-md border border-slate-200 bg-slate-50 p-4">
        <div className="mb-3 flex items-center gap-2 text-sm font-semibold text-slate-900">
          <Route className="h-4 w-4 text-slate-500" />
          {block.title}
        </div>
        <div className="grid gap-2 md:grid-cols-2 xl:grid-cols-3">
          {block.steps.map((step, index) => (
            <div key={step} className="rounded-md border border-slate-200 bg-white p-3">
              <div className="text-xs font-semibold uppercase text-slate-400">Шаг {index + 1}</div>
              <div className="mt-1 text-sm leading-5 text-slate-700">{highlightGuideText(step, query)}</div>
            </div>
          ))}
        </div>
      </div>
    )
  }

  return (
    <div className="rounded-md border border-sky-100 bg-sky-50 p-4">
      <div className="flex items-center gap-2 text-sm font-semibold text-sky-950">
        <Info className="h-4 w-4" />
        {block.title}
      </div>
      <p className="mt-2 text-sm leading-6 text-sky-900">{highlightGuideText(block.text, query)}</p>
    </div>
  )
}

